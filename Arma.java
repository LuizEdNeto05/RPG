public class Arma {
    String categoria;
    int danoConstante;

    public Arma()
    {
        this.categoria = " ";
        this.danoConstante = 0;
    }

    public Arma(String categoria, int danoConstante)
    {
        this.categoria = categoria;
        this.danoConstante = danoConstante;
    }
}
